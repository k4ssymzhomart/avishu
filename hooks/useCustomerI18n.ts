type CustomerCopy = (typeof customerCopy)[CustomerLanguage];

function getSystemOrderNote(
  language: CustomerLanguage,
  status: OrderStatus,
  productionNote?: string | null,
  deliveryNote?: string | null,
) {
  const copy = customerCopy[language].orders;

  if (status === 'placed') {
    return copy.currentOrderNotePlaced;
  }

  if (status === 'accepted') {
    return copy.currentOrderNoteAccepted;
  }

  if (status === 'in_production') {
    return productionNote?.trim().length
      ? productionNote.trim()
      : language === 'ru'
        ? 'Ателье сейчас работает над производственными этапами.'
        : 'The atelier is working through production steps now.';
  }

  if (status === 'ready') {
    return copy.currentOrderNoteReady;
  }

  if (status === 'out_for_delivery') {
    return deliveryNote?.trim().length
      ? deliveryNote.trim()
      : language === 'ru'
        ? 'Передача курьеру уже началась, а финальное окно доставки в процессе.'
        : 'Courier handoff is active and the final delivery window is underway.';
  }

  if (status === 'cancelled') {
    return copy.currentOrderNoteCancelled;
  }

  return deliveryNote?.trim().length ? deliveryNote.trim() : copy.currentOrderNoteDelivered;
}

export function useCustomerI18n() {
  const language = useCustomerPreferencesStore((state) => state.language);
  const setLanguage = useCustomerPreferencesStore((state) => state.setLanguage);
  const copy = useMemo<CustomerCopy>(() => customerCopy[language], [language]);

  const navItems = useMemo(
    () => [
      { href: '/customer', key: 'home', label: copy.nav.home },
      { href: '/customer/orders', key: 'orders', label: copy.nav.orders },
      { href: '/customer/chat', key: 'chat', label: copy.nav.chat },
      { href: '/customer/profile', key: 'profile', label: copy.nav.profile },
    ],
    [copy.nav.chat, copy.nav.home, copy.nav.orders, copy.nav.profile],
  );

  const categoryOptions = useMemo(
    () => [
      { key: 'ALL', label: copy.categories.all },
      { key: 'NEW', label: copy.categories.new },
      { key: 'OUTERWEAR', label: copy.categories.outerwear },
      { key: 'BASICS', label: copy.categories.basics },
      { key: 'CARDIGANS / KNIT', label: copy.categories.knit },
      { key: 'TROUSERS', label: copy.categories.trousers },
      { key: 'SKIRTS', label: copy.categories.skirts },
    ],
    [
      copy.categories.all,
      copy.categories.basics,
      copy.categories.knit,
      copy.categories.new,
      copy.categories.outerwear,
      copy.categories.skirts,
      copy.categories.trousers,
    ],
  );

  return {
    categoryOptions,
    copy,
    formatAvailabilityLabel: (availability: ProductAvailability) => formatAvailability(availability, language),
    formatChatTimestampLabel: (value: string) => formatChatTimestamp(value, language),
    formatCustomerStageLabel: (status: OrderStatus) => formatCustomerStage(status, language),
    formatDateLabel: (value?: string | null, withYear = false) => formatDateLabel(value, { language, withYear }),
    formatDeliveryMethodLabel: (method: DeliveryMethod) => formatDeliveryMethod(method, language),
    formatOrderNote: (status: OrderStatus, productionNote?: string | null, deliveryNote?: string | null) =>
      getSystemOrderNote(language, status, productionNote, deliveryNote),
    formatOrderStatusLabel: (status: OrderStatus) => formatOrderStatus(status, language),
    formatOrderTypeLabel: (type: OrderType) => formatOrderType(type, language),
    formatRelativeLabel: (value: string) => formatRelativeTime(value, language),
    language,
    navItems,
    setLanguage,
  };
}
